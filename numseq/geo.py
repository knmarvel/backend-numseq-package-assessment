def square(n):
    """given a number, n, returns the nth number in \
the sequence of square numbers"""
    return n ** 2


def triangle(n):
    """given a number, n, returns the nth number in \
the sequence of numbers completing a triangle"""
    return (n*(n+1)/2)


def cube(n):
    """given a number, n, returns the nth number in \
the sequence of cubed numbers"""
    return n ** 3
